export function LanguageProvider({ children }: { children: ReactNode }) {
    const [language, setLanguageState] = useState<Language>('ms');

    // Load language preference from localStorage on mount
    useEffect(() => {
        const savedLanguage = localStorage.getItem('agripadi_language') as Language | null;
        if (savedLanguage && (savedLanguage === 'ms' || savedLanguage === 'en')) {
            setLanguageState(savedLanguage);
            document.documentElement.lang = savedLanguage === 'ms' ? 'ms-MY' : 'en-US';
        }
    }, []);

    const setLanguage = (lang: Language) => {
        setLanguageState(lang);
        localStorage.setItem('agripadi_language', lang);
        document.documentElement.lang = lang === 'ms' ? 'ms-MY' : 'en-US';
    };

    const t = (key: string): string => {
        return translations[language][key] || key;
    };

    return (
        <LanguageContext.Provider value={{ language, setLanguage, t }}>
            {children}
        </LanguageContext.Provider>
    );
}

export function useLanguage() {
    const context = useContext(LanguageContext);
    if (!context) {
        throw new Error('useLanguage must be used within a LanguageProvider');
    }
    return context;
}
